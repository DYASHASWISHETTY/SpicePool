			<h1 style="text-align:center; font-family:georgian; font-style:italic; ">Here's the Max Amount of Sugar Kids Should Really Have In a Day</h1>
				<center><h2>The new recommendations are about a third of what the typical American child typically consumes.</h2></center>
	
			
			<center><img src="37.jpg" alt="SUGAR" style="padding-right:10%;" /></center>
			
			<p>Kids should have 6 teaspoons or less of added sugar a day, the American Heart Association said Monday—about one third of what the typical American child currently eats and drinks. 
			This is the same recommendation the organization previously made for adult women, and applies to all children between ages 2 and 18.</p>
			<br>
			<p>Those 6 teaspoons are equal to about 100 calories, or 25 grams, and include any sugars (like table sugar, fructose, and honey) that are used in the preparing or processing of foods or beverages. They do not, however, include naturally occurring sugars in foods such as dairy products and whole fruits</p>
			<br>
			<p>The scientific statement, published in the journal Circulation, also recommended that added sugars be completely omitted from the diets of children under 2. This age group needs fewer calories than older kids, the authors of the statement say, and there is little room for extra foods that don’t provide good nutrition. Limiting sugars at this age may also help children develop a life-long love for healthy food, since taste preferences are often formed early in life</p>
			<br>
			<p>Keeping track of these added sugars will be a lot easier starting in 2018, when food manufacturers will be required to list them, in grams, on their labels.</p>
			<br>
			<p>"Until then, the best way to avoid added sugars in your child's diet is to serve mostly foods that are high in nutrition, such as fruits, vegetables, whole grains, low-fat dairy products, lean meat, poultry and fish, and to limit foods with little nutritional value," Miriam Vos, M.D., nutrition scientist and associate professor of pediatrics at Emory University School of Medicine and lead author of the statement, said in a press release</p>
			<br>
			<p>Those “foods with little nutritional value” include sugar-sweetened beverages, such as soda, fruit-flavored and sports drinks, sweetened teas, and energy drinks. Kids should not drink more than 8 ounces of these drinks per week, Dr. Vos said—yet they are currently drinking the equivalent of their age in weekly servings.</p>
			<br>
			<p>Sweet processed foods, such as cereal and cereal bars, cookies, cakes and many foods marketed specifically to children, should also be avoided or limited to occasional treats.</p>
			<br>
			<p>Estimated calorie needs for children range from 1,000 a day for a sedentary 2-year-old to 3,200 for an active 16- to 18-year-old boy. But Dr. Vos said the AHA chose to make just one recommendation for all kids over age 2 in order to keep things simple for parents and public health advocates. For most children, she said, limiting sugar to less than 6 teaspoons a day is a healthy and achievable target.</p>
			<br>
			<p>"If your child is eating the right amount of calories to achieve or maintain a healthy body weight, there isn't much room in their food 'budget' for low-value junk foods, which is where most added sugars are found," she added.</p>
			<br>
			<p>Children, like adults, are more likely to develop risk factors for heart disease (such as obesity and high blood pressure) and Type 2 diabetes (such as insulin resistance) when they eat a lot of daily sweets or other forms of added sugar. Plus, said Dr. Vos, “children who eat foods loaded with added sugars tend to eat fewer healthy foods—such as fruits, vegetables, whole grains and low-fat dairy products—that are good for their heart health."</p>
			<br>
			<p>The authors did not make a recommendation for or against the use of no-calorie sweeteners, such as aspartame and sucralose, and were unsure whether the high sugar content in 100-percent fruit juice should be considered in the same category as sodas and sports drinks.</p>
			<br>
			<p>The new guideline will hopefully clear up some of the confusion and uncertainty that’s surrounded sugar recommendations for children, the authors wrote. They also noted that their statement aligns with current advice from the World Health Organization, the FDA, and the Dietary Guidelines for Americans, all of which recommend that added sugars make up less than 10 percent of daily calories</p>
			<br>
			<p>The American Heart Association says that limiting added sugars should be a goal for the whole family, as well. Like children, women are advised to have no more than six teaspoons a day. Men, whose overall calorie requirements tend to be a bit higher, should aim for no more than nine teaspoons or 150 calories.</p>
