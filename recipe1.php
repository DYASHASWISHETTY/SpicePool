			    <p style="font-size:40px; font-family:verdana; color:#916e91;" >Ingredients<br/></p>
				<p>36 pkg. OREO Cookies, divided<br/>
				1/4 cup butter, melted<br/>
				4 pkg. (8 oz. each) brick cream cheese, softened<br/>
				3/4 cup sugar<br/>
				1 tsp. vanilla<br/>
				1 tub (8 oz.) COOL WHIP Whipped Topping, thawed<br/>
				<b>Instructions</b><br/>
				CHOP  15 cookies coarsely. Finely crush remaining cookies; mix with butter. Press onto bottom of 13x9-inch pan. Refrigerate while preparing filling.<br/>
				BEAT  cream cheese, sugar and vanilla in large bowl with mixer until well blended. Gently stir in COOL WHIP and chopped cookies; spread onto crust.<br/>
				REFRIGERATE  4 hours or until firm.<br/></p></div>
			
			
