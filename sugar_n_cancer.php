			<h1 style="text-align:center; font-family:georgian; font-style:italic; ">This Is How Sugar May 'Fuel' Cancer Cells</h1>
				<center><h2>An expert explains the link between the sweet stuff and cancer risk.</h2></center>
	
			
			<center><img src="35.jpg" alt="Spoon And Sugar" style="padding-right:10%;" /></center>
			
			<p>If you saw the recent headlines connecting gallbladder cancer to the consumption of sugary sodas, you may not have been too surprised by the news. 
			America’s obsession with sugar has been blamed for plenty of chronic diseases in recent years, including several types of cancer.</p>
			<br>
			<p>But what is it, exactly, about the sweet stuff that seems to raise a person’s cancer risk? We rounded up some recent research and spoke with Carrie Daniel-MacDougall, PhD, a nutritional epidemiologist at The University of Texas’ MD Anderson Cancer Center, to get the scoop.</p>
			<br>
			<h2>What the studies show</h2>
			<p>Scientists have been studying the relationship between sugar and various cancers; some of them affect organs directly involved in the metabolism of sugar (like the liver and pancreas), while others do not.</p>
			<br>
			<p>A Swedish study published this month found that people who drank two or more servings of soda and other sugary drinks per day had a higher risk of gallbladder and biliary tract cancers than those who abstained from soda completely. And research published in March, co-authored by Daniel-MacDougall, found that people who ate the highest amount of foods with high glycemic indexes—typically foods high in simple carbohydrates like white bread, potatoes, and white rice—were more likely to get lung cancer than those who ate the least. (But let's be clear: Smoking causes lung cancer, not sugar. It's possible that a certain diet may make it harder for the body to suppress cancer, but a single study can't prove that.) This study didn’t focus directly on sweets like table sugar and desserts. But these foods do typically fall into the high–glycemic index/low-nutrient category.</p>
			<br>
			<p>Another study from MD Anderson Cancer Center, published in January, suggests a link between the typical Western (high-sugar) diet and breast cancer. When researchers split mice into four groups and fed them all different diets, they found that those who ate more sucrose or fructose (both types of simple sugars) had more breast cancer tumors—and more tumors that had spread to the lungs—than those who got most of their carbohydrates from starches.</p>
			<br>
			<p>Finally, according to several studies in recent years, people with diabetes or prediabetes are at increased risk for cancer—especially liver, pancreatic, and uterine cancer. An unhealthy diet is a major risk factor for developing high blood sugar and insulin resistance (both hallmarks of diabetes), says Daniel-MacDougall, so it’s not unrealistic to suggest that what you eat also affects your risk of these cancers</p>
			<br><br>
			<h2>How sugar may fuel cancer</h2>
			<p>News reports commonly refer to sugar as being “fuel” for cancer cells. And that’s true—but mainly because sugar is the fuel for all cells in the body. Sugar is a carbohydrate, and when you eat any type of carbohydrate (whether it’s fruit or ice cream or a bagel) your pancreas produces insulin, a hormone that helps convert sugars into energy for your cells.</p>
			<br>
			<p>Eating too much sugar, though, can cause the body to become insulin resistant, meaning it has to churn out more and more of the hormone in order to do its job. It also produces more of a hormone known as insulin-like growth factor (IGF), which “seems to stimulate cell growth and inhibit cell death,” says Daniel-MacDougall.</p>
			<br>
			<p>In other words, IGF allows cancer to proliferate. “Cancer cells are normally kept in check by the body’s constant turnover—new healthy cells grow and bad cells die,” she explains. “IGF blocks the signals for cells to grow normally and die when it’s time to die, so instead they just grow grow grow.”</p>
			<br>
			<p>High-sugar diets also contribute to inflammation throughout the body, which can also cause cellular damage and drive the growth of cancerous tumors. Sugary diets also lead to weight gain.</p>
			<br>
			<p>“When you eat foods that are high in sugar or high in carbohydrates and low in fiber, you have a higher insulin spike and your body stores more energy as fat,” says Daniel-MacDougall. “We know that obesity is a leading cause of cancer, so part of reducing your risk should be managing your weight.”</p>
			<br><br>
			<h2>The expert advice</h2>
			<p>Not all sugars and starches are bad. In fact, we need them in order to function as healthy human beings. But choosing the right kinds—and the right amount—is important.</p>
			<br>
			<p>Food with natural sugar (like fruit and dairy products) and complex carbohydrates (like whole grains, beans, and legumes), for example, provide calories plus vitamins, minerals, and nutrients like fiber, to help you feel full. Sodas, sweets, and white bread, on the other hand, only provide the former.</p>
			<br>
			<p>“Some people go on these highly restrictive low-carb or no-carb diets, but we don’t really know how effective these are at preventing cancer,” says Daniel-MacDougall. What’s more important, she adds, is finding a healthy diet you can stick with long-term.</p>
			<br>
			<p>“If you really want to lower your risk, you need to take a diversified approach,” she says. “Consume a diet of high-quality nutrients, work on losing or maintaining a healthy weight, and keep the empty-calorie foods in check.”</p>
