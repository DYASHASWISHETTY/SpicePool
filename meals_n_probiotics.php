			<h1 style="text-align:center; font-family:georgian; font-style:italic; ">5 Easy Meals That Include a Dose of Probiotics</h1>
				<center><h2>These super simple recipes help your body get the beneficial bugs it needs.</h2></center>
	
			
			<center><img src="24.jpg" alt="Probiotics" style="padding-right:10%;" /></center>
			
			<p>Probiotics are really hot right now—and unlike some nutrition fads, they actually live up to the hype. These “good" bacteria have been linked to better digestive health, immunity, mental well-being, and even weight control. By now, you’re probably well aware that there are plenty of probiotic options beyond yogurt. But sometimes sneaking them into your daily diet can be a challenge. 
			To help you out, I'm serving up five super simple meal ideas that incorporate a probiotic-rich food.</p>
			<br>
			<h2>Kefir breakfast bowl</h2>
			<p>Beautiful breakfast bowls are all the rage on Pinterest and Instagram. For a healthy, probiotic twist on this trend, build yours with kefir, a fermented milk beverage, as the base. (If you’re dairy-free there are plant milk versions, like coconut milk kefir, that contain the same probiotic benefits.) Stir in fresh grated ginger, ground cinnamon, or both! Top with sliced fruit, chopped nuts, seeds (like chia, sesame, or sunflower), and toasted old-fashioned rolled oats.</p>
			<br>
			<h2>Kimchi omelet</h2>
			<p>Kimchi is a staple in Korean cuisine, and a probiotic superstar thanks to the fermented combo of veggies and seasonings. For an easy way to fit it in add it to your morning meal, make a flavorful, probiotic-packed omelet. First sauté sliced mushrooms and fresh spinach in olive oil for a few minutes. Then add eggs or egg whites and a quarter cup of kimchi. Allow the eggs to set, and then carefully bring one side to the other, folding the omelet in half.</p>
			<br>
			<h2>Salmon sauerkraut lettuce cups</h2>
			<p>For a quick lunch, start by laying out three large outer Romaine leaves. Add an ounce of either canned, baked, or grilled wild salmon to each. Top each lettuce cup with minced red onion, a spoonful of hummus, and a dollop of sauerkraut. Or replace the lettuce with one slice of toasted whole grain or healthy gluten-free bread.</p>
			<br>
			<h2>Pickle-y tuna salad</h2>
			<p>Pickles can be a great probiotic option. But to reap the digestive and health benefits, be sure you grab a jar that says "fermented" (as opposed to "pickled"). Create a tasty dish by mixing tuna with a generous portion of chopped pickles, a tablespoon of balsamic vinegar, half a teaspoon of dried Italian herb seasoning, plus a teaspoon of spicy brown mustard, and lemon juice. Serve over a bed of your favorite greens, topped with other veggies like cucumber, grape tomatoes, and bell pepper. Add a little extra crunch with a few tablespoons of sliveredalmonds or sunflower seeds.</p>
			<br>
			<h2>Black bean tacos with fermented salsa</h2>
			<p>Fermented salsa is one of my favorite new probiotic finds. Look for it in health food stores near the sauerkraut and other fermented veggies. To use it in a meal, sauté sliced onions and bell peppers in low sodium organic vegetable broth over medium heat. Fill either three lettuce cups or two whole corn tortillas with half a cup of black beans. Top with onions, peppers, avocado slices or fresh guacamole, and, of course, the salsa!</p>
