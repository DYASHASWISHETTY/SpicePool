			<h1 style="text-align:center; font-family:georgian; font-style:italic; ">7 Secrets of People Who Bring Their Lunch to Work Every Day</h1>
				<center><h2>Here's how to become the type of person who packs a lunch.</h2></center>
	
			
			<center><img src="36.jpg" alt="Lunch" style="padding-right:10%;" /></center>
			
			<p>You probably have at least one coworker who, day after day, whips out a colorful Mason-jar salad or veggie-packed wrap. 
			She's on to something: 92% of restaurant meals have too many calories, according to a 2016 study published in the Journal of the American Academy of Nutrition and Dietetics. 
			Plus, researchers at Tufts University found that even "healthier" fast food options still pack too much salt and fat in a 2016 study. 
			“By having more control over portion sizes and what foods are going into each lunch, 
			you not only ensure what you're eating is healthier than what you might find in the cafeteria or vending area, 
			but also that what you're eating is satisfying to you personally,” says registered dietitian Regan Jones</p>
			<br>
			<p>If that's not enough to sway you to start packing your lunch, then maybe knowing just how much money you're spending will help. Let's do some math: If you buy lunch every workday, at 10 bucks a pop, that's $200 a month—and a whopping $2,400 a year, enough cash to take yourself and a friend on a Caribbean vacation</p>
			<br>
			<p>Now stock up on the best to-go containers for packing a lunch, and learn how to become the type of person who actually uses them:</p>
			<br><br>
			<h2>They plan the prep</h2>
			<p>You have three choices: prep and package your midday meals over the weekend so you can just grab-and-go every weekday morning; make your lunch every evening; or get up a little earlier and do it all in the morning. The choice is yours, and entirely depends on your personality, says Washington, D.C. registered dietitian and healthy living blogger Anne Mauney. “Some people find that they’re more successful prepping everything on Sunday, but others will find this overwhelming,” she notes. Experiment to find the approach that’s right for you. One tip: if you're packing the night before, do it while you're making dinner. It'll help solidify the habit—and will save you from double-cleanup.</p>
			<br><br>
			<h2>They love leftovers</h2>
			<p>Eating the same thing for lunch that you had for dinner last night can get really old, really fast. That's why people who bring their lunches to work know how to dress up their leftovers, says Jones. Her suggestions: “Tortilla wraps can turn a casserole into a burrito. Cooked pasta can be the base of a vegetarian pasta salad by adding roasted veggies, beans, and a sauce, like pesto. Most meals can be chopped and thrown on you choice of lettuce with dressing for an instant tossed salad.” Reinventing your leftovers tricks your tastebuds into believing you're having a totally new meal, minus all the extra prep.</p>
			<br><br>
			<h2>They have basic recipes that go beyond sandwiches</h2>
			<p>You can probably rattle off a bunch of basic sandwich recipes: turkey and Swiss on multigrain; tuna salad and arugula on rye; chicken breast and mustard on a whole-wheat bun. No-brainer recipes for other types of food will help keep your midday meal from getting repetitive. Mauney’s favorite is to make what she calls a “grand salad bowl.” She tosses together greens, 90-second plain brown rice, canned beans for protein, whatever veggies are lingering in her fridge, and a fat like avocado or cheese to boost satisfaction. She’ll put homemade vinaigrette or salsa in a little container, and toss the salad when it’s lunchtime.</p>
			<br><br>
			<h2>They eat away from their desks</h2>
			<p>Perhaps the chance to leave your workspace for a break is part of the reason why you're grabbing takeout for lunch every day. “You need that break during the day to de-stress and give yourself a breather,” says Mauney. Plus, eating while working is a mindless habit that can make you overeat. Take a break in the office kitchenette, or on nice days, bring it outside.</p>
			<br><br>
			<h2>They're part of a lunch club</h2>
			<p>Divide and conquer workday lunch with a coworker (or a few of them) who brown-bags it. On Monday, bring two servings of lunch to share; then it's her turn on Tuesday. “It’s great because you’re not always eating the same standard things you’d otherwise get bored of—and it’s half the work,” says Mauney. Just make sure your colleague is reliable and motivated—you'll be stuck grabbing takeout if your colleague forgets your food.</p>
			<br><br>
			<h2>They treat themselves</h2>
			<p>Pack a small treat with your lunch, Jones recommends, like a few squares of dark chocolate. “Small pieces work well and it will be less tempting to hit up the vending machines for a candy bar or snack later on,” she says.</p>
			<br><br>
			<h2>They plan for a freebie day (or two)</h2>
			<p>It’s a mistake to assume you’ll eat a packed lunch 100% of the time. Inevitably, your friend will ask you to meet for sushi, or you'll have to take a client out for food. And that’s okay! Mauney recommends aiming to bring lunch two to four times per week, depending on what your circumstances are at work. Just like everything else in life, your lunch break requires a little flexibility. </p>
